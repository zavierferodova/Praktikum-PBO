class Karyawan {
    String nama;
    String alamat;
    String jabatan;
    double gaji;

    void tampilkanNama() {
        System.out.println("Nama karyawan adalah " + nama);
    }

    void tampilkanJabatan() {
        System.out.println("Jabatan karyawan adalah " + jabatan);
    }

    void tampilkanAlamat() {
        System.out.println("Alamat karyawan adalah " + alamat);
    }

    void tampilkanGaji() {
        System.out.println("Gaji karyawan sebesar Rp. " + gaji);
    }
}