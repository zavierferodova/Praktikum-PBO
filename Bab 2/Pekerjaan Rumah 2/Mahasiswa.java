class Mahasiswa {
    String nama;
    String nim;
    String alamat;
    int semester;

    void tampilkanNama() {
        System.out.println("Nama mahasiswa adalah " + nama);
    }

    void tampilkanNim() {
        System.out.println("NIM mahasiswa adalah " + nim);
    }

    void tampilkanAlamat() {
        System.out.println("Alamat mahasiswa adalah " + alamat);
    }

    void tampilkanSemester() {
        System.out.println("Semester mahasiswa adalah " + semester);
    }
}